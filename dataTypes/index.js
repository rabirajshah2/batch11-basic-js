// 8 types of Data Types

// 1.String 
const name="Rabi Raj";
console.log(name);

// 2.Number
const num=100;
console.log(num);

// 3.Bigint
const int=BigInt("5768146465116154151");
console.log(int);


// 4.Boolean
const isMale=true;
const isFemale=false;
console.log(isMale, isFemale);

// 5.Undefined
let x;
console.log(x);

// 6.Null
const y=null;
console.log(y);

// 7.Symbol
const n=Symbol("Rabi");
console.log(n);

// 8.Object
const Rabi={name:"Rabi", age:23, dob:""};
console.log(Rabi);

